#include <iostream>
using namespace std;
int main(){
    
    int a;
    cout<<"Size of Int: "<<sizeof(a)<<"\n";
    
    float b;
    cout<<"Size of Float: "<<sizeof(b)<<"\n";
    
    char c;
    cout<<"Size of Character: "<<sizeof(c)<<"\n";
    
    bool d;
    cout<<"Size of Boolean: "<<sizeof(d)<<"\n";
    
    
    
    short int si;
    cout<<"Size of short Int: "<<sizeof(si)<<"\n";
    
    long int li;
    cout<<"Size of long Int: "<<sizeof(li)<<"\n";
    
    return 0;
    
}
